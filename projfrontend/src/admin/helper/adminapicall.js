//category api calls

//update a category
export const updateThisCategory = (categoryId, userId, token, apple) => {
  console.log(`category=${apple.name}`);
  const xyz = `${API}category/${categoryId}/${userId}`;
  return fetch(`${xyz}`, {
    method: "PUT",
    headers: {
      //  Accept: "application/json;charset=UTF-8",
      "Content-Type": "application/json; charset=UTF-8",
      Authorization: `Bearer ${token}`,
    },
    body: JSON.stringify(apple),
  })
    .then((response) => {
      const abc = response.json();
      return abc;
    })
    .catch((err) => console.log(`error=${err}`));
};

